#include "minishell.h"

int	_is_overflow(char *str)
{
	if (str[0] == '-' && (ft_strlen(str) <= 20))
	{
		if (ft_strncmp(str, "-9223372036854775808", 20) > 0)
			return (1);
	}
	else if (ft_strlen(str) <= 19)
	{
		if (ft_strncmp(str, "9223372036854775807", 19) > 0)
			return (1);
	}
	return (0);
}
