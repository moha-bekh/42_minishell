#include "minishell.h"

int	_varstr_conv(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (!ft_isalpha(str[i]) && str[i] != '_')
			return (1);
		i++;
	}
	return (0);
}
